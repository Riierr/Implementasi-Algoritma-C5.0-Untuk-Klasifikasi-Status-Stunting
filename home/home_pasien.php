<script>
// Smooth scroll untuk navigasi internal
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        e.preventDefault();
        const target = document.querySelector(this.getAttribute('href'));
        if (target) {
            target.scrollIntoView({
                behavior: 'smooth',
                block: 'start'
            });
        }
    });
});

// Animasi untuk info boxes
document.querySelectorAll('.info-box, .small-box').forEach(box => {
    box.addEventListener('mouseenter', function() {
        this.style.transform = 'translateY(-5px)';
        this.style.transition = 'transform 0.3s ease';
    });
    
    box.addEventListener('mouseleave', function() {
        this.style.transform = 'translateY(0)';
    });
});

// Alert otomatis untuk pengunjung baru
window.onload = function() {
    const isFirstVisit = !sessionStorage.getItem('visited');
    if (isFirstVisit) {
        setTimeout(() => {
            alert("Selamat datang! Mari bersama cegah stunting sejak dini.");
            sessionStorage.setItem('visited', 'true');
        }, 1000);
    }
};
</script>

